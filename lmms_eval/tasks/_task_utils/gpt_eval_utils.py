GPT_EVAL_NUM_SECONDS_TO_SLEEP = int(os.getenv("GPT_EVAL_NUM_SECONDS_TO_SLEEP", 5))
GPT_EVAL_NUM_RETRIES = int(os.getenv("GPT_EVAL_NUM_RETRIES", 5))
GPT_EVAL_TIMEOUT = int(os.getenv("GPT_EVAL_TIMEOUT", 30))
GPT_EVAL_RAISE_AFTER_MAX_RETRIES = bool(int(os.getenv("GPT_EVAL_RAISE_AFTER_MAX_RETRIES", 0)))
GPT_EVAL_USER = os.getenv("GPT_EVAL_USER", "lmms-eval")
GPT_EVAL_PRINT_TRACEBACK = bool(int(os.getenv("GPT_EVAL_PRINT_TRACEBACK", 0)))



def encode_image(image: Image.Image):
    bytes_data = io.BytesIO()
    image.save(bytes_data, format="JPEG")
    image_base64 = "data:image/jpeg;base64," + base64.b64encode(bytes_data.getvalue()).decode('utf-8')
    return image_base64


class OpenAIClient:
    def __init__(self, api_url, api_key, model=None, task=None, user=GPT_EVAL_USER):
        self.api_url = api_url
        self.api_key = api_key
        self.user = user
        self.task = task
        self.model = model
    
    def get_chat_response(
            self, 
            conv,
            check_response=None,
            postprocess_response=None, 
            default_response="",
            retries=GPT_EVAL_NUM_RETRIES, 
            sleep_time=GPT_EVAL_NUM_SECONDS_TO_SLEEP, 
            raise_after_max_retries=GPT_EVAL_RAISE_AFTER_MAX_RETRIES,
            print_traceback=GPT_EVAL_PRINT_TRACEBACK,
            generation_kwargs=None,
        ):
        for attempt in range(retries):
            try:
                response = self.chat(conv, generation_kwargs)
                if check_response is not None:
                    check_response(response)
                if postprocess_response is not None:
                    response = postprocess_response(response)
                return response
            except Exception as e:
                from traceback import format_exc
                eval_logger.error(f"Error on attempt {attempt+1}: {e}")
                if print_traceback:
                    eval_logger.error(format_exc())
                time.sleep(sleep_time)
                
        eval_logger.error(f"Failed to get response after {retries} attempts")
        if raise_after_max_retries:
            raise Exception(f"Failed to get response after {retries} attempts")
        else:
            return default_response
    

    def chat(self, conv, generation_kwargs=None):
        url = f"{self.api_url}/chat/completions"
        headers = {
            "Authorization": f"Bearer {self.api_key}",
            "Content-Type": "application/json",
        }

        if generation_kwargs is None:
            generation_kwargs = {}

        data = {
            "messages": conv,
            **generation_kwargs,
        }
        if self.model is not None:
            data["model"] = self.model

        response = requests.post(url, headers=headers, json=data, timeout=GPT_EVAL_TIMEOUT)
        if response.status_code != 200:
            raise Exception(f"Failed to chat with OpenAI: {response.content.decode()}")
        # print(response.content.decode())
        # print(response.json())
        return response.json()['choices'][0]['message']['content']
    
    def evaluate_correctness(
            self, 
            question, answer, prediction,
            check_response=None,
            postprocess_response=None, 
            default_response="",
            retries=GPT_EVAL_NUM_RETRIES, 
            sleep_time=GPT_EVAL_NUM_SECONDS_TO_SLEEP, 
            raise_after_max_retries=GPT_EVAL_RAISE_AFTER_MAX_RETRIES,
            print_traceback=GPT_EVAL_PRINT_TRACEBACK,
            generation_kwargs=None,
        ):
        content = json.dumps({
            "Question": question,
            "Answer": answer,
            "Prediction": prediction
        }, indent=4)
        prompt = "You will be given the question, the answer and the prediction. If the answer is a list, all the elements in the list are correct answers. Your task is to evaluate the correctness of the prediction based on both the question and the answer. The score is 1 if the prediction is a valid answer to the question, otherwise 0. Note that the prediction does not have to exactly match the given answer for it to be valid. Provide a concise analysis, then give your score in \\boxed{}. \n" + \
            content
        messages = [
            {"role": "system", "content": "You are an assistant which evaluates the correctness of predictions generated by multi-modal LLMs. "},
            {"role": "user", "content": prompt}
        ]
        if generation_kwargs is None:
            generation_kwargs = {
                "max_tokens": 512,
                "temperature": 0.0,
                "top_p": 1.0,
            }
        response = self.get_chat_response(
            messages,
            check_response=check_response,
            postprocess_response=postprocess_response,
            default_response=default_response,
            retries=retries,
            sleep_time=sleep_time,
            raise_after_max_retries=raise_after_max_retries,
            print_traceback=print_traceback,
            generation_kwargs=generation_kwargs,
        )
        return response
        
